package src.basics_05_23Dec;

public class Lab088 {

    public static void main(String[] args) {


        // While an Do while

        int i =1; // A- initialization is out side of loop
        while( i<=10 ){ // B- condition

            System.out.println(i); //D- code execute
            i++; // increment
        }

        for (int j = 1; j<=10; j++){ // j = j+2 we can write any increment
            System.out.println(j);
        }


    }
}
